        private void StarsMuertasLejos(Player player)
        {

            //Valida si hay estrellas y si ya se murieron las elimina
            if (estrellas.Count > 0)
            {
                for (int j = 0; j < estrellas.Count; j++)
                {
                    if (estrellas[j].enemDie)
                    {
                        timeDeleteStars += 0.25F;

                        if (timeDeleteStars > 20)
                        {
                            estrellas.Remove(estrellas[j]);

                            timeDeleteStars = 0;
                        }
                    }
                }
            }

            //Valida si hay estrellas y si estan muy lejos se eliminan
            if (estrellas.Count > 0)
            {
                for (int i = 0; i < estrellas.Count; i++)
                {
                    if ((estrellas[i].enemPosicion.X) + 15 < (player.posicion.X) - RANGO_X)
                    {
                        estrellas.Remove(estrellas[i]);
                    }
                }
            }


        }


